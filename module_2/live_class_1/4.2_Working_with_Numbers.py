# Integer Division (//)
result = 17 // 5     # Divides and rounds down
print(result)        # Shows: 3

# Modulus (%)
remainder = 17 % 5   # Shows remainder after division
print(remainder)     # Shows: 2

# Exponents (**)
square = 5 ** 2     # 5 squared
cube = 2 ** 3       # 2 cubed
print(square)       # Shows: 25
print(cube)         # Shows: 8