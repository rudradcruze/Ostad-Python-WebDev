# Creating Strings
# Using single or double quotes
name1 = "Alex"        # Double quotes
name2 = 'Alex'        # Single quotes

# Quotes inside strings
message1 = "Don't forget to smile"    # Using double quotes outside
message2 = 'He said "Hello!" to me'   # Using single quotes outside

# Multi-line strings
long_text = """This is a long message
that can span across
multiple lines!"""

print(name1)        # Shows: Alex
print(name2)        # Shows: Alex
print(message1)     # Shows: Don't forget to smile
print(message2)     # Shows: He said "Hello!" to me

# Using multi-line strings
print(long_text)    # Shows: This is a long message