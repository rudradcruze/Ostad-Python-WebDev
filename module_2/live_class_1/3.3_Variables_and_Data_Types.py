is_raining = True
has_homework = False
game_over = False

# You can change boolean values
game_over = True    # Game is now over

print(is_raining)   # Shows: True
print(game_over)    # Shows: True
print(has_homework) # Shows: False

# You can also use boolean values in if statements
if is_raining:
    print("Bring an umbrella!")  # Shows: Bring an umbrella!