str = "Bangladesh"

# find() method
print(str.find("Bangladesh"))  # 0

# find() method
print(str.find("desh"))  # 6

# Starts with
# startswith() method
print(str.startswith("Bang"))  # True

# endswith() method
print(str.endswith("desh"))  # True

# SPLIT
# split() method
print("A, B, C, D".split(", "))  # ['A', 'B', 'C', 'D']

# JOIN
# join() method
print(", ".join(["A", "B", "C", "D"]))