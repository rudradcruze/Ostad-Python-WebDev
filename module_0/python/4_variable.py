# Rules for variable names:

'''
    1. Descriptive Names
    2. Lowercase with Underscores
    3. Avoid Reserved Words
    4. Start with a Letter or Underscore
    5. Case Sensitive
    6. Short but meaningful
    7. Use Singular Nouns, plural for lists
    8. Consistency
    9. Avoid double underscore
'''
student = 100
students = ["Alice", "Bob", "Charlie"]

print(student)
print(students)